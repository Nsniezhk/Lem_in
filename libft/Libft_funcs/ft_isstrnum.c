#include "libft.h"

int			ft_isstrnum(char *l)
{
	int i;
	int	res;

	i = 0;
	res = 0;
	if (l != NULL && l[0] != '\0')
	{
		if ((l[0] == '-' || l[0] == '+') && l[1] != '\0')
		{
			i = 1;
			res = (l[0] == '-') ? -1 : 1;
		}
		while (ft_isdigit(l[i]) && l[i] != '\0')
			i++;
		if (l[i] == ' ' && l[i] != '\0')
		{
			while (l[i] == ' ' && l[i] != '\0')
				i++;
		}
		if (l[i] == '\0' && res != -1)
			res = 1;
		return (res);
	}
	return (-2);
}
