#include "libft.h"

size_t	ft_wordcount(char const *s, char c)
{
	size_t result;
	size_t i;

	i = 0;
	result = 0;
	while (s[i] && s[i] == c)
		i++;
	while (s[i])
	{
		while (s[i] && s[i] != c)
			i++;
		result++;
		while (s[i] && s[i] == c)
			i++;
	}
	return (result);
}
